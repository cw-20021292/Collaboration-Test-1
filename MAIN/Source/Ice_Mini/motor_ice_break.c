bit F_IceBreak_Motor_Out;
/***********************************************************************************************************************/

/**
 * @brief 얼음분쇄 모터 동작
 * 
 */
void    MOTOR_ICE_BREAK_OUTPUT(void);

/***********************************************************************************************************************/
/**
 * @brief 얼음분쇄 모터 동작
 * 
 */
void    MOTOR_ICE_BREAK_OUTPUT(void)
{
    if(F_IceBreak == SET)
	{
        if(F_IceBreak_Motor_Out == SET)
        {
            P_OUT_ICE_CRUSH = SET;
        }
        else
        {
            P_OUT_ICE_CRUSH = CLEAR;
        }
    }
    else
    {
        P_OUT_ICE_CRUSH = CLEAR;
    }
}
